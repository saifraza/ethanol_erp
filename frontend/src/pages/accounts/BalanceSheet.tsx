/**
 * Balance Sheet — Tally-style primary groups (Indian format).
 *
 * Tree levels:
 *   L1  Primary head       (Capital Account, Loans (Liability), Current Liabilities,
 *                           Fixed Assets, Investments, Current Assets, ...)
 *   L2  Sub-group          (Duties & Taxes, Sundry Creditors, Bank Accounts, ...)
 *   L3  Ledger / Party     (TDS Payable, Acme Traders, UBI A/C 4438, Plant & Machinery)
 *   L4  Transactions       (date, narration, dr, cr, running balance)
 *   L5  Full journal entry (the double-entry, showing the other-side accounts)
 *
 * Classification runs purely in the frontend from `subType` + account name.
 * No backend change. When the CoA gets a proper `primaryGroup` field,
 * this classifier becomes a one-line lookup.
 */

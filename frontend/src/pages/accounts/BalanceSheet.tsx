interface BSAccount {
  id: string;
  code: string;
  name: string;
  subType: string | null;
  balance: number;
}

interface BSSection {
  accounts: BSAccount[];
  total: number;
}

interface BSData {
  asOnDate: string;
  assets: BSSection;
  liabilities: BSSection;
  equity: BSSection;
  liabilitiesAndEquity: number;
  isBalanced: boolean;
}


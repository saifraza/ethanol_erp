// Payment mode → account mapping
const PAYMENT_ACCOUNT: Record<string, string> = {
  'CASH': ACCT.CASH,
  'UPI': ACCT.SBI_BANK,
  'NEFT': ACCT.SBI_BANK,
  'RTGS': ACCT.SBI_BANK,
  'CHEQUE': ACCT.SBI_BANK,
  'BANK_TRANSFER': ACCT.SBI_BANK,
};

// Product → sales account mapping
function getSalesAccountCode(productName: string): string {
  const p = (productName || '').toUpperCase();
  if (p.includes('ETHANOL') || p.includes('ENA') || p.includes('RS') || p.includes('RECTIFIED')) return ACCT.ETHANOL_SALES;
  if (p.includes('DDGS') || p.includes('DISTILLER')) return ACCT.DDGS_SALES;
  return ACCT.OTHER_INCOME;
}

// Category → expense account mapping
function getExpenseAccountCode(category: string): string {
  const c = (category || '').toUpperCase();
  if (c.includes('GRAIN') || c.includes('RAW')) return ACCT.GRAIN_PURCHASE;
  if (c.includes('CHEMICAL')) return ACCT.CHEMICAL_PURCHASE;
  if (c.includes('SPARE') || c.includes('MAINTENANCE')) return ACCT.MAINTENANCE_EXPENSE;
  if (c.includes('UTILITY')) return ACCT.UTILITY_EXPENSE;
  return ACCT.OTHER_EXPENSE;
}

interface AccountCache {
  [code: string]: string; // code → id
}

/** Resolve account codes to IDs (cached per call) */
async function resolveAccounts(prisma: PrismaClient, codes: string[]): Promise<AccountCache> {
  const uniqueCodes = [...new Set(codes)];
  const accounts = await prisma.account.findMany({
    where: { code: { in: uniqueCodes }, isActive: true },
    select: { id: true, code: true },
  });
  const cache: AccountCache = {};
  for (const a of accounts) cache[a.code] = a.id;
  return cache;
}

/** Helper: create journal entry with lines in a transaction */
async function createJournalEntry(
  tx: any,
  params: {
    date: Date;
    narration: string;
    refType: string;
    refId: string;
    userId: string;
    lines: { accountId: string; debit: number; credit: number; narration?: string; costCenter?: string }[];
  }
): Promise<string> {
  // Dedup check: skip if a non-reversed entry already exists for same refType+refId
  if (params.refType && params.refId) {
    const existing = await tx.journalEntry.findFirst({
      where: { refType: params.refType, refId: params.refId, isReversed: false },
    });
    if (existing) {
      console.warn(`[AutoJournal] Skipping duplicate: ${params.refType}/${params.refId} already has entry #${existing.entryNo}`);
      return existing.id;
    }
  }

  // Validate debits = credits
  const totalDebit = params.lines.reduce((s, l) => s + l.debit, 0);
  const totalCredit = params.lines.reduce((s, l) => s + l.credit, 0);
  if (Math.abs(totalDebit - totalCredit) > 0.01) {
    throw new Error(`Journal unbalanced: debit=${totalDebit}, credit=${totalCredit}`);
  }

  const entry = await tx.journalEntry.create({
    data: {
      date: params.date,
      narration: params.narration,
      refType: params.refType,
      refId: params.refId,
      isAutoGenerated: true,
      userId: params.userId,
      lines: {
        create: params.lines.map(l => ({
          accountId: l.accountId,
          debit: Math.round(l.debit * 100) / 100,
          credit: Math.round(l.credit * 100) / 100,
          narration: l.narration,
          costCenter: l.costCenter || 'DISTILLERY',
        })),
      },
    },
  });

  return entry.id;
}

// ═══════════════════════════════════════════════════════
// SALE INVOICE → Journal Entry
// Dr Trade Receivable (totalAmount)
//   Cr Sales Revenue (amount before GST)
//   Cr GST Output CGST (cgst)
//   Cr GST Output SGST (sgst) — or IGST for interstate
// ═══════════════════════════════════════════════════════
export async function onSaleInvoiceCreated(
  prisma: PrismaClient,
  invoice: {
    id: string;
    invoiceNo: number;
    totalAmount: number;
    amount: number;
    gstAmount: number;
    gstPercent: number;
    cgstAmount?: number;
    sgstAmount?: number;
    igstAmount?: number;
    supplyType?: string;
    freightCharge?: number;
    productName: string;
    customerId: string;
    userId: string;
    invoiceDate: Date;
    customer?: { state?: string | null } | null;
  }
): Promise<string | null> {
  try {
    const isInterstate = invoice.supplyType === 'INTER_STATE' || (!invoice.supplyType && invoice.customer?.state && invoice.customer.state !== 'Madhya Pradesh');
    const salesCode = getSalesAccountCode(invoice.productName);
    const gstCodes = isInterstate
      ? [ACCT.GST_OUTPUT_IGST]
      : [ACCT.GST_OUTPUT_CGST, ACCT.GST_OUTPUT_SGST];

    const allCodes = [ACCT.TRADE_RECEIVABLE, salesCode, ...gstCodes];
    const accts = await resolveAccounts(prisma, allCodes);

    // Check all accounts exist
    for (const code of allCodes) {
      if (!accts[code]) return null; // Accounts not seeded yet
    }

    // Credit sales revenue includes freight recovery to keep journal balanced
    const salesCredit = invoice.amount + (invoice.freightCharge || 0);
    const lines: { accountId: string; debit: number; credit: number; narration?: string }[] = [
      { accountId: accts[ACCT.TRADE_RECEIVABLE], debit: invoice.totalAmount, credit: 0, narration: `INV-${invoice.invoiceNo}` },
      { accountId: accts[salesCode], debit: 0, credit: salesCredit, narration: `${invoice.productName} sale${invoice.freightCharge ? ' + freight' : ''}` },
    ];

    if (isInterstate) {
      const igst = invoice.igstAmount ?? invoice.gstAmount;
      lines.push({ accountId: accts[ACCT.GST_OUTPUT_IGST], debit: 0, credit: igst, narration: `IGST @${invoice.gstPercent}%` });
    } else {
      const cgst = invoice.cgstAmount ?? invoice.gstAmount / 2;
      const sgst = invoice.sgstAmount ?? (invoice.gstAmount - cgst);
      lines.push({ accountId: accts[ACCT.GST_OUTPUT_CGST], debit: 0, credit: cgst, narration: `CGST @${invoice.gstPercent / 2}%` });
      lines.push({ accountId: accts[ACCT.GST_OUTPUT_SGST], debit: 0, credit: sgst, narration: `SGST @${invoice.gstPercent / 2}%` });
    }

    return await prisma.$transaction(async (tx: any) => {
      return createJournalEntry(tx, {
        date: invoice.invoiceDate,
        narration: `Sale Invoice INV-${invoice.invoiceNo} — ${invoice.productName}`,
        refType: 'SALE',
        refId: invoice.id,
        userId: invoice.userId,
        lines,
      });
    });
  } catch (err) {
    console.error('[AutoJournal] Failed to create sale invoice journal:', err);
    return null;
  }
}

// ═══════════════════════════════════════════════════════
// SALE PAYMENT RECEIVED → Journal Entry
// Dr Bank/Cash (amount)
//   Cr Trade Receivable (amount)
// ═══════════════════════════════════════════════════════
export async function onSalePaymentReceived(
  prisma: PrismaClient,
  payment: {
    id: string;
    amount: number;
    mode: string;
    reference?: string | null;
    paymentDate: Date;
    customerId: string;
    invoiceId?: string | null;
    userId: string;
  }
): Promise<string | null> {
  try {
    const bankCode = PAYMENT_ACCOUNT[payment.mode] || ACCT.SBI_BANK;
    const accts = await resolveAccounts(prisma, [bankCode, ACCT.TRADE_RECEIVABLE]);
    if (!accts[bankCode] || !accts[ACCT.TRADE_RECEIVABLE]) return null;

    return await prisma.$transaction(async (tx: any) => {
      return createJournalEntry(tx, {
        date: payment.paymentDate,
        narration: `Payment received — ${payment.mode} ${payment.reference || ''}`.trim(),
        refType: 'RECEIPT',
        refId: payment.id,
        userId: payment.userId,
        lines: [
          { accountId: accts[bankCode], debit: payment.amount, credit: 0, narration: `${payment.mode} ${payment.reference || ''}`.trim() },
          { accountId: accts[ACCT.TRADE_RECEIVABLE], debit: 0, credit: payment.amount },
        ],
      });
    });
  } catch (err) {
    console.error('[AutoJournal] Failed to create sale payment journal:', err);
    return null;
  }
}


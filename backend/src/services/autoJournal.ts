// ═══════════════════════════════════════════════════════
// GRN / PURCHASE INVOICE → Journal Entry
// Dr Expense/Inventory (amount before GST)
// Dr GST Input CGST + SGST (or IGST)
//   Cr Trade Payable (total)
// ═══════════════════════════════════════════════════════
export async function onPurchaseBooked(
  prisma: PrismaClient,
  purchase: {
    id: string;
    refNo: string;
    totalAmount: number;
    baseAmount: number;
    gstAmount: number;
    gstPercent: number;
    category: string;
    vendorId: string;
    userId: string;
    date: Date;
    isInterstate?: boolean;
  }
): Promise<string | null> {
  try {
    const expenseCode = getExpenseAccountCode(purchase.category);
    const isInterstate = purchase.isInterstate || false;
    const gstCodes = isInterstate
      ? [ACCT.GST_INPUT_IGST]
      : [ACCT.GST_INPUT_CGST, ACCT.GST_INPUT_SGST];

    const allCodes = [expenseCode, ACCT.TRADE_PAYABLE, ...gstCodes];
    const accts = await resolveAccounts(prisma, allCodes);
    for (const code of allCodes) {
      if (!accts[code]) return null;
    }

    const lines: { accountId: string; debit: number; credit: number; narration?: string }[] = [
      { accountId: accts[expenseCode], debit: purchase.baseAmount, credit: 0, narration: purchase.refNo },
    ];

    if (purchase.gstAmount > 0) {
      if (isInterstate) {
        lines.push({ accountId: accts[ACCT.GST_INPUT_IGST], debit: purchase.gstAmount, credit: 0, narration: `IGST @${purchase.gstPercent}%` });
      } else {
        const halfGst = purchase.gstAmount / 2;
        lines.push({ accountId: accts[ACCT.GST_INPUT_CGST], debit: halfGst, credit: 0, narration: `CGST @${purchase.gstPercent / 2}%` });
        lines.push({ accountId: accts[ACCT.GST_INPUT_SGST], debit: halfGst, credit: 0, narration: `SGST @${purchase.gstPercent / 2}%` });
      }
    }

    lines.push({ accountId: accts[ACCT.TRADE_PAYABLE], debit: 0, credit: purchase.totalAmount, narration: purchase.refNo });

    return await prisma.$transaction(async (tx: any) => {
      return createJournalEntry(tx, {
        date: purchase.date,
        narration: `Purchase — ${purchase.refNo}`,
        refType: 'PURCHASE',
        refId: purchase.id,
        userId: purchase.userId,
        lines,
      });
    });
  } catch (err) {
    console.error('[AutoJournal] Failed to create purchase journal:', err);
    return null;
  }
}

// ═══════════════════════════════════════════════════════
// VENDOR PAYMENT → Journal Entry
// Dr Trade Payable (amount + tds)
//   Cr Bank/Cash (amount)
//   Cr TDS Payable (tds, if any)
// ═══════════════════════════════════════════════════════
export async function onVendorPaymentMade(
  prisma: PrismaClient,
  payment: {
    id: string;
    amount: number;
    mode: string;
    reference?: string | null;
    tdsDeducted: number;
    vendorId: string;
    userId: string;
    paymentDate: Date;
  }
): Promise<string | null> {
  try {
    const bankCode = PAYMENT_ACCOUNT[payment.mode] || ACCT.SBI_BANK;
    const codes = [bankCode, ACCT.TRADE_PAYABLE];
    if (payment.tdsDeducted > 0) codes.push(ACCT.TDS_PAYABLE);

    const accts = await resolveAccounts(prisma, codes);
    for (const code of codes) {
      if (!accts[code]) return null;
    }

    const totalSettled = payment.amount + payment.tdsDeducted;
    const lines: { accountId: string; debit: number; credit: number; narration?: string }[] = [
      { accountId: accts[ACCT.TRADE_PAYABLE], debit: totalSettled, credit: 0, narration: `Payment ${payment.reference || ''}`.trim() },
      { accountId: accts[bankCode], debit: 0, credit: payment.amount, narration: `${payment.mode} ${payment.reference || ''}`.trim() },
    ];

    if (payment.tdsDeducted > 0) {
      lines.push({ accountId: accts[ACCT.TDS_PAYABLE], debit: 0, credit: payment.tdsDeducted, narration: `TDS deducted` });
    }

    return await prisma.$transaction(async (tx: any) => {
      return createJournalEntry(tx, {
        date: payment.paymentDate,
        narration: `Vendor payment — ${payment.mode} ${payment.reference || ''}`.trim(),
        refType: 'PAYMENT',
        refId: payment.id,
        userId: payment.userId,
        lines,
      });
    });
  } catch (err) {
    console.error('[AutoJournal] Failed to create vendor payment journal:', err);
    return null;
  }
}

// ═══════════════════════════════════════════════════════
// STOCK MOVEMENT → Journal Entry
// Receipt: Dr Inventory, Cr GRN Clearing/Payable
// Issue: Dr COGS/WIP, Cr Inventory
// Adjustment: Dr/Cr Inventory, Cr/Dr Inventory Adjustment
// ═══════════════════════════════════════════════════════
export async function onStockMovement(
  prisma: PrismaClient,
  movement: {
    id: string;
    movementNo: number;
    movementType: string;
    direction: string;
    totalValue: number;
    itemName: string;
    userId: string;
    date: Date;
  }
): Promise<string | null> {
  try {
    if (movement.totalValue <= 0) return null; // No value movements don't need journals

    const accts = await resolveAccounts(prisma, [ACCT.INVENTORY, ACCT.COGS, ACCT.TRADE_PAYABLE, ACCT.OTHER_EXPENSE]);
    if (!accts[ACCT.INVENTORY]) return null;

    let lines: { accountId: string; debit: number; credit: number; narration?: string; costCenter?: string }[] = [];

    if (movement.direction === 'IN') {
      // Dr Inventory, Cr Payable (for GRN) or Other (for production/adjustment)
      const creditAcct = movement.movementType === 'GRN_RECEIPT'
        ? (accts[ACCT.TRADE_PAYABLE] || accts[ACCT.OTHER_EXPENSE])
        : (accts[ACCT.OTHER_EXPENSE] || accts[ACCT.TRADE_PAYABLE]);

      if (!creditAcct) return null;

      lines = [
        { accountId: accts[ACCT.INVENTORY], debit: movement.totalValue, credit: 0, narration: `${movement.movementType} — ${movement.itemName}` },
        { accountId: creditAcct, debit: 0, credit: movement.totalValue, narration: `Stock receipt #${movement.movementNo}` },
      ];
    } else {
      // Dr COGS/Expense, Cr Inventory
      const debitAcct = movement.movementType === 'SALES_ISSUE'
        ? (accts[ACCT.COGS] || accts[ACCT.OTHER_EXPENSE])
        : (accts[ACCT.OTHER_EXPENSE] || accts[ACCT.COGS]);

      if (!debitAcct) return null;

      lines = [
        { accountId: debitAcct, debit: movement.totalValue, credit: 0, narration: `${movement.movementType} — ${movement.itemName}` },
        { accountId: accts[ACCT.INVENTORY], debit: 0, credit: movement.totalValue, narration: `Stock issue #${movement.movementNo}` },
      ];
    }

    return await prisma.$transaction(async (tx: any) => {
      return createJournalEntry(tx, {
        date: movement.date,
        narration: `Stock ${movement.direction === 'IN' ? 'receipt' : 'issue'} #${movement.movementNo} — ${movement.itemName}`,
        refType: movement.movementType === 'GRN_RECEIPT' ? 'PURCHASE' : movement.movementType === 'SALES_ISSUE' ? 'SALE' : 'JOURNAL',
        refId: movement.id,
        userId: movement.userId,
        lines,
      });
    });
  } catch (err) {
    console.error('[AutoJournal] Failed to create stock movement journal:', err);
    return null;
  }
}


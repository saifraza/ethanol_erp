/**
 * Auto-Journal Engine — Automated Double-Entry Bookkeeping
 *
 * Every financial transaction in the ERP automatically generates journal entries.
 * This eliminates manual bookkeeping and ensures the general ledger is always in sync.
 *
 * Trigger points:
 * - Sale Invoice created → Dr Receivable, Cr Sales + GST
 * - Sale Payment received → Dr Bank/Cash, Cr Receivable
 * - GRN confirmed → Dr Inventory/Expense, Cr Payable
 * - Vendor Payment made → Dr Payable, Cr Bank/Cash
 * - Shipment payment confirmed → Dr Bank/Cash, Cr Receivable
 * - Stock movement → Dr/Cr Inventory, Cr/Dr COGS/WIP
 */


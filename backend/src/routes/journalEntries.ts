// ── POST / — Create manual journal entry ──
router.post('/', validate(createJournalSchema), asyncHandler(async (req: AuthRequest, res: Response) => {
  const { date, narration, refType, refId, lines } = req.body;

  // Validate: total debits must equal total credits
  const totalDebit = lines.reduce((s: number, l: { debit: number }) => s + l.debit, 0);
  const totalCredit = lines.reduce((s: number, l: { credit: number }) => s + l.credit, 0);

  if (Math.abs(totalDebit - totalCredit) > 0.01) {
    throw new ValidationError(
      `Debits (₹${totalDebit.toFixed(2)}) must equal Credits (₹${totalCredit.toFixed(2)}). Difference: ₹${Math.abs(totalDebit - totalCredit).toFixed(2)}`
    );
  }

  // Validate: each line must have either debit OR credit (not both)
  for (const line of lines) {
    if (line.debit > 0 && line.credit > 0) {
      throw new ValidationError('Each journal line must have either debit or credit, not both');
    }
    if (line.debit === 0 && line.credit === 0) {
      throw new ValidationError('Each journal line must have a non-zero debit or credit');
    }
  }

  // Validate all account IDs exist
  const accountIds = [...new Set(lines.map((l: { accountId: string }) => l.accountId))];
  const accounts = await prisma.account.findMany({
    where: { id: { in: accountIds as string[] } },
    select: { id: true },
  
    take: 500,
  });
  if (accounts.length !== accountIds.length) {
    throw new ValidationError('One or more account IDs are invalid');
  }

  const companyId = getActiveCompanyId(req);
  const entryNo = await nextDocNo('JournalEntry', 'entryNo', companyId);

  const entry = await prisma.$transaction(async (tx) => {
    const journal = await tx.journalEntry.create({
      data: {
        entryNo,
        date: new Date(date),
        narration,
        refType: refType || 'JOURNAL',
        refId: refId || null,
        userId: req.user?.id || 'system',
        companyId,
        lines: {
          create: lines.map((l: { accountId: string; debit: number; credit: number; narration?: string; costCenter?: string; division?: string }) => ({
            accountId: l.accountId,
            debit: l.debit,
            credit: l.credit,
            narration: l.narration || null,
            costCenter: l.costCenter || null,
            division: l.division || null,
          })),
        },
      },
      include: {
        lines: {
          include: {
            account: { select: { code: true, name: true } },
          },
        },
      },
    });
    return journal;
  });

  res.status(201).json(entry);
}));

// ── POST /:id/reverse — Reverse a journal entry (create opposite entry) ──
router.post('/:id/reverse', asyncHandler(async (req: AuthRequest, res: Response) => {
  const original = await prisma.journalEntry.findUnique({
    where: { id: req.params.id },
    include: { lines: true },
  });
  if (!original) throw new NotFoundError('Journal Entry', req.params.id);
  if (!canAccessCompany(req, original.companyId)) throw new NotFoundError('Journal Entry', req.params.id);

  if (original.isReversed) {
    throw new ValidationError('This entry has already been reversed');
  }

  const reversal = await prisma.$transaction(async (tx) => {
    // Mark original as reversed
    await tx.journalEntry.update({
      where: { id: original.id },
      data: { isReversed: true },
    });

    // Create reversal entry (swap debits and credits)
    const rev = await tx.journalEntry.create({
      data: {
        date: new Date(),
        narration: `Reversal of Entry #${original.entryNo}: ${original.narration}`,
        refType: original.refType,
        refId: original.refId,
        isAutoGenerated: false,
        reversalOf: original.id,
        userId: req.user?.id || 'system',
        companyId: original.companyId,
        lines: {
          create: original.lines.map((l: { accountId: string; debit: number; credit: number; costCenter: string | null }) => ({
            accountId: l.accountId,
            debit: l.credit,   // swap
            credit: l.debit,   // swap
            narration: `Reversal`,
            costCenter: l.costCenter,
          })),
        },
      },
      include: {
        lines: {
          include: { account: { select: { code: true, name: true } } },
        },
      },
    });
    return rev;
  });

  res.status(201).json({ message: 'Entry reversed', original: original.id, reversal });
}));


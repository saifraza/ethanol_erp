    "issALt" DOUBLE PRECISION,
    "issAStrength" DOUBLE PRECISION,
    "issAVolume" DOUBLE PRECISION,
    "issBDip" DOUBLE PRECISION,
    "issBLt" DOUBLE PRECISION,
    "issBStrength" DOUBLE PRECISION,
    "issBVolume" DOUBLE PRECISION,
    "issCDip" DOUBLE PRECISION,

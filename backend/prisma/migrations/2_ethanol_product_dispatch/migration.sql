CREATE TABLE IF NOT EXISTS "DispatchTruck" (
    "id" TEXT NOT NULL,
    "entryId" TEXT NOT NULL,
    "vehicleNo" TEXT NOT NULL DEFAULT '',
    "partyName" TEXT NOT NULL DEFAULT '',
    "destination" TEXT NOT NULL DEFAULT '',
    "quantityBL" DOUBLE PRECISION NOT NULL DEFAULT 0,
    "strength" DOUBLE PRECISION,
    "remarks" TEXT,
    "createdAt" TIMESTAMP(3) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT "DispatchTruck_pkey" PRIMARY KEY ("id")
);

ALTER TABLE "DispatchTruck" ADD CONSTRAINT "DispatchTruck_entryId_fkey"
    FOREIGN KEY ("entryId") REFERENCES "EthanolProductEntry"("id") ON DELETE CASCADE ON UPDATE CASCADE;
